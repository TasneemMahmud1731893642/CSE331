ORG 100H
MOV AX, @DATA
MOV DS, AX

MOV SI, OFFSET ARR1 ; REVERSE
MOV DI, OFFSET ARR2 ; INPUT

MOV CX, 0

INPUT: 
MOV AH,1
INT 21H
CMP AL,13
JE L1
MOV [DI],AL
INC DI 
INC CX
JMP INPUT


L1:
DEC DI
PUSH CX
MOV AH, 2
MOV DL, 0AH
INT 21H
MOV DL, 0DH
INT 21H

LX:
MOV BL, [DI]
MOV [SI], BL
INC SI
DEC DI
LOOP LX

POP CX

MOV SI, OFFSET ARR1 ; REVERSE
MOV DI, OFFSET ARR2 ; INPUT

CHECK:
MOV BL, [SI]
MOV BH, [DI]
CMP BL, BH
JNE NOTPAL
INC DI
INC SI
LOOP CHECK

LEA DX, PAL
MOV AH, 9
INT 21H
JMP EXIT

NOTPAL:
LEA DX, NOPAL
MOV AH, 9
INT 21H
JMP EXIT


EXIT:

RET
ARR1 DB 40 DUP('$')  ;REVERSE
ARR2 DB 40 DUP('$')  ;FOR INPUT   
LEA DX,ARR1
MOV AH,9   
INT 21H
PAL DB 'IT IS PALINDROME$'
NOPAL DB 'IT IS NOT PALINDROME$'